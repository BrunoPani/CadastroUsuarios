    public void logar(){
        try {
            String sql = "Select * from usuarios where login = ? and senha =?";
        pst = conexao.prepareStatement(sql);
        
        pst.setString(1, jTextUsuario.getText());
        pst.setString(2, new String(jTextSenha.getPassword()));
        
        rs = pst.executeQuery();
        
        if (rs.next()){
            TelaPrincipal tp = new TelaPrincipal(); 
            tp.setVisible(true);
            dispose();
        }else{
            JOptionPane.showMessageDialog(null, "Usuario ou senha inv√°lido!");
            jTextUsuario.setText(null);
            jTextSenha.setText(null);
            jTextUsuario.requestFocus();
        }
        
        } catch (Exception e) {
        }
    }
